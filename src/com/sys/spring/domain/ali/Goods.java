package com.sys.spring.domain.ali;

public class Goods {

	private int id ;
	private int whoid ;
	private String name ;
	private String info ;
	private int state ;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getWhoid() {
		return whoid;
	}
	public void setWhoid(int whoid) {
		this.whoid = whoid;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getInfo() {
		return info;
	}
	public void setInfo(String info) {
		this.info = info;
	}
	public int getState() {
		return state;
	}
	public void setState(int state) {
		this.state = state;
	}
	
}
